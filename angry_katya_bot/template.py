import db


class Content:
    """
    Базовый класс для блоков с информации
    """

    def __init__(self, content_data, subject):

        self._content_type = content_data[0].title()
        self._content_num = int(content_data[-1])
        self._subject = subject

    def show(self):
        return getattr(db,
                       self._content_type).objects(owner=db.User.objects(usr_id=self._subject).get())[self._content_num]
